Livraison
Tarifs et modes de livraison proposés
Nous livrons dans la plupart des pays et nous proposons deux modes de livraison : 

- Une livraison sécurisée directement à votre domicile pour la plupart des pays.

- Une livraison sécurisée avec assurance payante à votre domicile.

Pour consulter la liste des pays où nous livrons merci de cliquer ICI.

La livraison est gratuite pour les commandes de plus de 99€ d'achats pour la France métropolitaine et de plus de 129€ pour les autres pays. Pour connaître le montant exact de la livraison, vous devez vous rendre sur la page de paiement, les frais de livraison seront ensuite calculés automatiquement en fonction des produits de votre panier et du pays de livraison sélectionné.

Délais d'expédition et de livraison
Le temps de préparation et d'expédition varie entre 1 à 3 jours ouvrés. Concernant la livraison, il faut compter 7 à 10 jours ouvrés pour les pays de l'Union européenne et entre 7 à 20 jours ouvrés pour les USA et le Canada. Ces délais sont à titre purement informatif et peuvent varier en fonction de la période.

Vous pouvez trouver plus de détails sur nos délais de livraison en cliquant ICI.

